class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            DiceRollerTheme {
                DiceScreen()
            }
        }
    }
}

@Preview(showBackground = true)
@Composable
fun DiceScreen() {
    DiceRollerTheme {
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(12.dp),
            horizontalAlignment = Alignment.CenterHorizontally,
            verticalArrangement = Arrangement.Center
        ) {
            var result = remember { mutableIntStateOf(1) }
            val resImg = when (result.intValue) {
                1 -> R.drawable.dice_1
                2 -> R.drawable.dice_2
                3 -> R.drawable.dice_3
                4 -> R.drawable.dice_4
                5 -> R.drawable.dice_5
                else -> R.drawable.dice_6
            }
            Image(
                painter = painterResource(resImg),
                contentDescription = result.intValue.toString()
            )
            Button(
                modifier = Modifier.padding(20.dp),
                onClick = {
                    result.intValue = (1..6).random()
                }
            ) {
                Text(text = "Roll")
            }
        }
    }
}